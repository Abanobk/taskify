class Autogenerated {
  bool? error;
  List<CommentModel>? comments;

  Autogenerated({this.error, this.comments});

  Autogenerated.fromJson(Map<String, dynamic> json) {
    error = json['error'];
    if (json['comments'] != null) {
      comments = <CommentModel>[];
      json['comments'].forEach((v) {
        comments!.add(CommentModel.fromJson(v));
      });
    }
  }

  Map<String, dynamic> toJson() {
    final data = <String, dynamic>{};
    data['error'] = error;
    if (comments != null) {
      data['comments'] = comments!.map((v) => v.toJson()).toList();
    }
    return data;
  }

  Autogenerated copyWith({
    bool? error,
    List<CommentModel>? comments,
  }) {
    return Autogenerated(
      error: error ?? this.error,
      comments: comments ?? this.comments,
    );
  }
}

class CommentModel {
  final String? id;
  final String? content;
  final String? sentTime;
  final String? createdAt;
  final Commenter? commenter;
  final List<CommentsAttachments>? attachments;
  final List<CommentModel>? children;
  final bool isTemporary; // Added isTemporary field

  CommentModel({
    this.id,
    this.content,
    this.sentTime,
    this.createdAt,
    this.commenter,
    this.attachments,
    this.children,
    this.isTemporary = false, // Default to false
  });

  CommentModel copyWith({
    String? id,
    String? content,
    String? sentTime,
    String? createdAt,
    Commenter? commenter,
    List<CommentsAttachments>? attachments,
    List<CommentModel>? children,
    bool? isTemporary,
  }) {
    return CommentModel(
      id: id ?? this.id,
      content: content ?? this.content,
      sentTime: sentTime ?? this.sentTime,
      createdAt: createdAt ?? this.createdAt,
      commenter: commenter ?? this.commenter,
      attachments: attachments ?? this.attachments,
      children: children ?? this.children,
      isTemporary: isTemporary ?? this.isTemporary,
    );
  }

  factory CommentModel.fromJson(Map<String, dynamic> json) {
    return CommentModel(
      id: json['id']?.toString(),
      content: json['content'],
      sentTime: json['sent_time'],
      createdAt: json['created_at'],
      commenter: json['commenter'] != null
          ? Commenter.fromJson(json['commenter'])
          : null,
      attachments: json['attachments'] != null
          ? (json['attachments'] as List)
          .map((e) => CommentsAttachments.fromJson(e))
          .toList()
          : [],
      children: json['children'] != null
          ? (json['children'] as List)
          .map((e) => CommentModel.fromJson(e))
          .toList()
          : [],
      isTemporary: json['isTemporary'] ?? false,
    );
  }
  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'content': content,
      'sent_time': sentTime,
      'created_at': createdAt,
      'commenter': commenter?.toJson(),
      'attachments': attachments?.map((e) => e.toJson()).toList(),
      'children': children?.map((e) => e.toJson()).toList(),
      'isTemporary': isTemporary,
    };
  }
  void printCommentModel() {
