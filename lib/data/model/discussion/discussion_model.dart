class Children {
  int? id;
  String? content;
  Commenter? commenter;
  String? createdAt;
  String? sentTime;
  List<CommentsAttachments>? attachments;
  List<Children>? children;

  Children({
    this.id,
    this.content,
    this.commenter,
    this.createdAt,
    this.sentTime,
    this.attachments,
    this.children,
  });

  Children.fromJson(Map<String, dynamic> json) {
    id = json['id'];
    content = json['content'];
    commenter = json['commenter'] != null
        ? Commenter.fromJson(json['commenter'])
        : null;
    createdAt = json['created_at'];
    sentTime = json['sent_time'];
    if (json['attachments'] != null) {
      attachments = <CommentsAttachments>[];
      json['attachments'].forEach((v) {
        attachments!.add(CommentsAttachments.fromJson(v));
      });
    }
    if (json['children'] != null) {
      children = <Children>[];
      json['children'].forEach((v) {
        children!.add(Children.fromJson(v));
      });
    }
  }

  Map<String, dynamic> toJson() {
    final data = <String, dynamic>{};
    data['id'] = id;
    data['content'] = content;
    if (commenter != null) {
      data['commenter'] = commenter!.toJson();
    }
    data['created_at'] = createdAt;
    data['sent_time'] = sentTime;
    if (attachments != null) {
      data['attachments'] = attachments!.map((v) => v.toJson()).toList();
    }
    if (children != null) {
      data['children'] = children!.map((v) => v.toJson()).toList();
    }
    return data;
  }

  Children copyWith({
    int? id,
    String? content,
    Commenter? commenter,
    String? createdAt,
    String? sentTime,
    List<CommentsAttachments>? attachments,
    List<Children>? children,
  }) {
    return Children(
      id: id ?? this.id,
      content: content ?? this.content,
      commenter: commenter ?? this.commenter,
      createdAt: createdAt ?? this.createdAt,
      sentTime: sentTime ?? this.sentTime,
      attachments: attachments ?? this.attachments,
      children: children ?? this.children,
    );
  }
}
